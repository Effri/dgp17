<h3>СПб ГБУЗ "Детская городская поликлиника №17" Выборгского района г. Санкт-Петербурга является государственным бюджетным учреждением здравоохранения. Учреждена&nbsp; Приказом&nbsp; ГУЗЛ&nbsp; № 672 от 23.05.1977 г.</h3>
 <br>
 <b>Главный врач&nbsp;<strong>Корнилова Эльмира Григорьевна</strong>&nbsp;врач высшей квалификационной категории, отличник здравоохранения.</b><br>
 <br>
 Мощность поликлиники рассчитана на 320 посещений в смену.<br>
 Расположена в 3-х этажном здании типовой постройки (год застройки 1977), по адресу: 194358, улица Есенина дом 38 корпус 2.&nbsp;&nbsp;<br>
 Своей деятельностью обеспечивает лечебно – профилактическую помощь детям от 0 до 18 лет, численность населения на 01.01.2020 г. составляет 14 336 человек.<br>
