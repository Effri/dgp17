<p style="color: #5d5d5d;">
	<span style="color: #000000;"> </span><strong><span style="color: #000000;">4. Порядок разрешения конфликтов между пациентом и Поликлиникой</span></strong><span style="color: #000000;"> </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	Порядок рассмотрения жалоб и обращений определен в соответствие &nbsp;с Федеральным&nbsp;законом&nbsp;Российской Федерации от 21 ноября 2011 г. N 323-ФЗ "Об основах охраны здоровья граждан в Российской Федерации". </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	&nbsp;Федеральным Законом Российской Федерации “О порядке рассмотрения обращений граждан Российской Федерации&nbsp;от 02.05.2006г. 59-ФЗ. </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	Распоряжение Комитета здравоохранения от 26.09.2007 г. № 492-р " Об утверждении форм учета и отчетности по работе с обращениями граждан". </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	4.1. В случае конфликтных ситуаций пациент (его законный представитель) имеет право непосредственно обратиться в администрацию Поликлиники или к дежурному администратору согласно графику приема граждан или обратиться к администрации поликлиники в письменном виде. </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	4.2. При личном приеме гражданин предъявляет документ, удостоверяющий его личность. Содержание устного обращения заносится в регистрационную карту обращения граждан. В случае,&nbsp;если изложенные в устном обращении факты и обстоятельства являются очевидными и не требуют дополнительной проверки, ответ на обращение с согласия гражданина может быть дан устно в ходе личного приема. В остальных случаях дается письменный ответ по существу поставленных в обращении вопросов. </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	4.3. Письменное обращение, принятое в ходе личного приема, подлежит регистрации и рассмотрению в порядке, установленном Федеральным законом. </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	4.4. В случае, если в обращении содержатся вопросы, решение которых не входит в компетенцию должностного лица, гражданину дается разъяснение, куда и в каком порядке ему следует обратиться. </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	4.5. Гражданин в своем письменном обращении в обязательном порядке указывает либо наименования учреждений, в которые направляет письменное обращение, либо фамилию, имя, отчество соответствующего должностного лица, либо должность соответствующего лица, а также свои фамилию, имя, отчество (последнее - при наличии), почтовый адрес, по которому должны быть направлены ответ, уведомление о переадресации обращения, излагает суть предложения, заявления или жалобы, ставит личную подпись и дату. </span>
</p>
<span style="color: #000000;"> </span>
<p>
	<span style="color: #000000;">
	4.6. В случае необходимости в подтверждение своих доводов гражданин прилагает к письменному обращению документы и материалы либо их копии. </span>
</p>
<span style="color: #000000;"> </span>
<p>
