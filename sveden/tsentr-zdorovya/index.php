 <b><u>Целью деятельности Центра здоровья является:</u></b>&nbsp;формирование здорового образа жизни<br>
 у детей, выполнение комплекса мероприятий, направленных на сохранение здоровья, пропаганда здорового образа жизни, мотивирование граждан к личной ответственности за свое здоровье<br>
 и здоровье своих детей, разработка индивидуальных подходов по формированию здорового образа жизни у детей, борьба с факторами риска неинфекционных заболеваний, информирование детского населения о вреде употребления табака и алкоголя, предотвращение социально значимых заболеваний среди детского населения.<br>
 Центр Здоровья оборудован самой современной компьютеризированной медицинской аппаратурой, позволяющей за одно посещение выявить наличие факторов риска определенных заболеваний. Диагноз при этом не ставится, а только определяются возможные факторы риска развития хронических неинфекционных заболеваний.<br>
 <b>Все обследования и консультации в Центре здоровья проходят бесплатно.</b><br>
 <br>
 <b><u>В Центр здоровья направляются</u></b>&nbsp;здоровые и дети с функциональными отклонениями<br>
 в состоянии здоровья. Врач-педиатр, на основании результатов тестирования<br>
 на аппаратно-программного комплекса&nbsp;<b><u>(обследование сердечно-сосудистой,<br>
 дыхательной, нервной, эндокринной, опорно-двигательной системы, состава тела,<br>
 адаптации к физическим и нервно-психическим нагрузкам)&nbsp;</u></b>проводит оценку функциональных и адаптивных резервов организма, определяет наиболее вероятные факты риска развития заболеваний и&nbsp;<b><u>составляет индивидуальную программу по здоровому образу жизни детей и подростков.</u></b><br>
 При необходимости врач-педиатр рекомендует динамическое наблюдение в Центре здоровья<br>
 с проведением повторных исследований или посещения занятий в соответствующих школах здоровья, лечебно-физкультурных залах. Если в процессе обследования в Центре здоровья выявляется подозрение на заболевание, врач-педиатр Центра здоровья направляет ребенка в ЛПУ по месту проживания к врачу-специалисту для определения дальнейшей тактики его наблюдения и лечения.<br>
