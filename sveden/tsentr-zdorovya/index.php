<h4><b>Интересные факты:</b></h4>
 </u>В 2016 году в центре прошли обследование&nbsp;<b>3374 человека,</b>&nbsp;из них у&nbsp;<b>1889 (55,9%)</b>&nbsp;выявлены факторы риска развития той или иной патологии. За 7 мес. 2017 – обследование прошли<br>
 <b>2751 ребенок,</b>&nbsp;факторы риска выявлены у&nbsp;<b>1532 детей(55,7%).</b><br>
 За отчетный период сохраняется относительно стабильная динамика общего уровня здоровья детского населения. Одной из форм пропаганды здорового образа жизни является участие Центра здоровья в проведении районных и городских акциях. Так в 2016 г. было проведено 13 Акций<br>
 (из них 2 Акции выездного характера посвященные Международному дню защиты детей и Всемирному дню сердца) с общим количеством участников 3.5 тысяч человек. Все Акции освещались в средствах массовой информации в информационно-новостных передачах кабельного телеканала «Пакт» и канала «Санкт-Петербург».<br>
 Опыт организации и проведения массовых мероприятий и акций был представлен докладом на тему: «Организация и проведение массовых акций среди населения в детской поликлинике» на городской конференции, проводимой Городским центром медицинской профилактики 29.01.2016года.<br>
